<h1>B, I, U, PRE</h1>
<h1><em>Définition:</em></h1>
<p>
<em>Les balises <strong>B, I, U, PRE</strong> définissent un style appliqué au texte</em><br>
<em>B est la balise qui sert à mettre en <b>gras</b>.</em><br>
<em>I est la balise qui sert à mettre en<i>italique</i>.</em><br>
<em>U est la balise qui sert à <u>souligner</u>.</em><br>
<em>PRE est la balise qui sert à laisser le texte s'afficher comme il est, sans aucun mise en forme.</em><br><br><br><br><br><br>
</p>    
</article>
